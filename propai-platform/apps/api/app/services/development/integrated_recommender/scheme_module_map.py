"""정책 사업방식(한글) → 수지 모듈 M-code 매핑.

`scenario_simulator`가 판정하는 정책 사업방식명(도시개발사업·지구단위계획·가로주택정비사업·
모아주택·역세권 활성화사업 등)을, 수지분석 모듈(M01~M15) 코드로 환원한다. 한 정책방식이
실현되는 건축 유형은 보통 복수라 리스트로 매핑한다.

★1차 증분에서 통합추천 오케스트레이터는 후보 개발유형을 permit_validator.get_permitted_types
(용도지역 허용유형)로 산정한다. 이 매핑은 '정책방식→모듈' 정합을 위한 보조 사전이며,
대응 M-code가 없는 scheme은 의도적으로 제외한다(없는 모듈을 지어내지 않음 — 정직).

M-code 의미(permit_validator.DEVELOPMENT_TYPE_NAMES 기준):
  M01 재개발  M02 재건축  M03 역세권개발  M04 지역주택조합  M05 임대협동조합
  M06 일반분양  M07 주상복합  M08 오피스텔  M09 지식산업센터  M10 단독주택
  M11 전원주택  M12 타운하우스  M13 도시형생활주택  M14 공공임대  M15 민간리츠
"""
from __future__ import annotations

# 정책 사업방식명 → 대응 수지 모듈 코드 리스트.
# (없는 scheme은 키 자체를 두지 않는다 — get_modules_for_scheme이 빈 리스트 반환.)
SCHEME_TO_MODULES: dict[str, list[str]] = {
    # 정비사업 계열 — 재개발/재건축.
    "정비사업": ["M01", "M02"],
    "재개발": ["M01"],
    "재건축": ["M02"],
    "소규모재개발사업": ["M01", "M13"],
    "소규모재건축사업": ["M02", "M13"],
    # 도시개발법 — 대규모 일단지 분양개발(공동주택·주상복합).
    "도시개발사업(도시개발법)": ["M06", "M07"],
    "도시개발구역 지정": ["M06", "M07"],
    # 지구단위계획 — 통합개발 기반(공동주택·주상복합·오피스텔).
    "지구단위계획 연계": ["M06", "M07", "M08"],
    "지구단위계획 수립·심의": ["M06", "M07", "M08"],
    # 가로주택정비사업 — 소규모 공동주택 분양.
    "가로주택정비사업": ["M06", "M13"],
    # 모아주택/모아타운 — 소규모 블록 통합 공동주택.
    "모아주택/모아타운": ["M06", "M13"],
    # 역세권 활성화/임대 — 용도상향 고밀복합(주상복합·오피스텔·임대).
    "역세권 활성화사업": ["M03", "M07", "M08"],
    "역세권 장기전세주택(시프트)": ["M03", "M14"],
    "역세권 청년안심주택": ["M03", "M14"],
    # 단순건축(정책 미적용 폴백) — 일반분양·도시형생활주택.
    "단순건축": ["M06", "M13"],
    "일반건축": ["M06", "M13"],
}


def get_modules_for_scheme(scheme: str) -> list[str]:
    """정책 사업방식명에 대응하는 수지 모듈 코드 리스트(없으면 빈 리스트)."""
    s = (scheme or "").strip()
    if s in SCHEME_TO_MODULES:
        return list(SCHEME_TO_MODULES[s])
    # 부분 일치(접두/포함) — 표기 변형(예: "역세권 활성화") 대응. 가장 먼저 맞는 키.
    for key, mods in SCHEME_TO_MODULES.items():
        if key in s or s in key:
            return list(mods)
    return []  # 대응 모듈 없음 — 정직(지어내지 않음).
